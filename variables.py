player_name = "Link"
# Translation: Create a box labeled "player_name" and put "Link" inside
weapon = "Master Sword"

# INTEGERS = whole numbers (no quotes)
health = 100
# Translation: Create a box labeled "health" and put 100 inside
gold = 50

# FLOATS = decimal numbers
attack_power = 15.5
# The decimal point makes this a "float" not an "int"

