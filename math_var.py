# Basic math
health = 100
damage = 25
health = health - damage  # Take damage
print("Health remaining:", health)

# Shortcuts
gold = 50
gold += 10  # Same as: gold = gold + 10
gold -= 5   # Subtract 5
gold *= 2   # Multiply by 2
print("Gold:", gold)

# Combining text (concatenation)
first_name = "John"
last_name = "Wick"
full_name = first_name + " " + last_name
print(full_name)